		Event.stopObserving(document.documentElement, 'mouseup', this.bind("dragStop"), false)
		document.onselectstart = function() {return true}

		this.cursorStartX = null
		this.cursorStartY = null
		this.boxStartX = null
		this.boxStartY = null
